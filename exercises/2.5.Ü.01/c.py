# Convert the int variable to a float and a str data type and print the result
integer_var = 10                # Integer
float_var = float(integer_var)  # Convert to Float
string_var = str(integer_var)   # Convert to String
# Print the variables
print(integer_var, type(integer_var))
print(float_var, type(float_var))
print(string_var, type(string_var))